uint8_t mul(uint8_t a, uint8_t b)
{
    uint8_t result = 0;
    while(b)
    {
        if(b & 1) result ^= a;
        a = xtime(a);
        b >>= 1;
    }
    return result;
}


void InvMixColumns(uint8_t state[4][4])
{
    int i;
    uint8_t temp[4];

    for(i = 0; i < 4; i++)
    {
        // save column values
        uint8_t s0 = state[0][i];
        uint8_t s1 = state[1][i];
        uint8_t s2 = state[2][i];
        uint8_t s3 = state[3][i];

        // row 0: 0E 0B 0D 09
        temp[0] = mul(0x0E, s0) ^ mul(0x0B, s1) ^ mul(0x0D, s2) ^ mul(0x09, s3);

        // row 1: 09 0E 0B 0D
        temp[1] = mul(0x09, s0) ^ mul(0x0E, s1) ^ mul(0x0B, s2) ^ mul(0x0D, s3);

        // row 2: 0D 09 0E 0B
        temp[2] = mul(0x0D, s0) ^ mul(0x09, s1) ^ mul(0x0E, s2) ^ mul(0x0B, s3);

        // row 3: 0B 0D 09 0E
        temp[3] = mul(0x0B, s0) ^ mul(0x0D, s1) ^ mul(0x09, s2) ^ mul(0x0E, s3);

        // write back
        state[0][i] = temp[0];
        state[1][i] = temp[1];
        state[2][i] = temp[2];
        state[3][i] = temp[3];
    }
}

void Decrypt(uint8_t state[4][4], uint8_t roundKeys[176])
{
    int round, i, j;
    uint8_t roundKey[4][4];

    // Round 10 key first
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            roundKey[j][i] = roundKeys[160 + i*4 + j];
    ADDRK(state, roundKey);

    // Rounds 9 down to 1
    for(round = 9; round >= 1; round--)
    {
        InvShiftRows(state);
        InvSubBytes(state);

        for(i = 0; i < 4; i++)
            for(j = 0; j < 4; j++)
                roundKey[j][i] = roundKeys[round*16 + i*4 + j];

        ADDRK(state, roundKey);
        InvMixColumns(state);
    }

    // Round 0 — no InvMixColumns
    InvShiftRows(state);
    InvSubBytes(state);
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            roundKey[j][i] = roundKeys[i*4 + j];
    ADDRK(state, roundKey);
}

