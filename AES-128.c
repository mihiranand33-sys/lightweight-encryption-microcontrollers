void ADDRK(uint8_t state[4][4], uint8_t roundKey[4][4])
{
    int i,j;
    for (i = 0; i < 4; i++)
    {
        for (j = 0; j < 4; j++)
        {
            state[i][j] ^= roundKey[i][j];
        }
    }
}
void SubBytes(uint8_t state[4][4])
{
    int i,j;
    for (i = 0; i < 4; i++)
    {
        for (j = 0; j < 4; j++)
        {
            state[i][j] = Sbox[state[i][j]];
        }
    }
}

void ShiftRows(uint8_t state[4][4])
{
    uint8_t temp;

    // Row 1: shift left by 1
    temp = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = temp;

    // Row 2: shift left by 2
    temp = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = temp;
    temp = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = temp;

    // Row 3: shift left by 3 (or right by 1)
    temp = state[3][3];
    state[3][3] = state[3][2];
    state[3][2] = state[3][1];
    state[3][1] = state[3][0];
    state[3][0] = temp;
}

//It multiplies a byte by 2 in GF(2⁸).
uint8_t xtime(uint8_t x)
{
    if(x & 0x80)
    //to prevent overflow, we XOR with 0x1B (the irreducible polynomial x^8 + x^4 + x^3 + x + 1).
        return (x << 1) ^ 0x1B;
    else
        return x << 1;
}

void MixColumns(uint8_t state[4][4])
{
    int i;
    uint8_t temp[4];
    for (i = 0; i < 4; i++)
    {
        temp[0] = xtime(state[0][i]) ^ xtime(state[1][i]) ^ state[1][i] ^ state[2][i] ^ state[3][i];
        temp[1] = state[0][i] ^ xtime(state[1][i]) ^ xtime(state[2][i]) ^ state[2][i] ^ state[3][i];
        temp[2] = state[0][i] ^ state[1][i] ^ xtime(state[2][i]) ^ xtime(state[3][i]) ^ state[3][i];
        temp[3] = xtime(state[0][i]) ^ state[0][i] ^ state[1][i] ^ state[2][i] ^ xtime(state[3][i]);
        state[0][i] = temp[0];
        state[1][i] = temp[1];
        state[2][i] = temp[2];
        state[3][i] = temp[3];
     }
}

uint8_t RC[10] = {
    0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
};
//generates the 11 round keys needed by AES-128.

void KeyExpansion(uint8_t key[16], uint8_t EXkeys[176])
{
    int i;
    uint8_t temp[4];

    for(i = 0; i < 16; i++)
        EXkeys[i] = key[i];

    for(i = 16; i < 176; i += 4)
    {
        temp[0] = EXkeys[i-4];
        temp[1] = EXkeys[i-3];
        temp[2] = EXkeys[i-2];
        temp[3] = EXkeys[i-1];

        if(i % 16 == 0)
        {
            // RotWord
            uint8_t t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;

            // SubWord
            temp[0] = Sbox[temp[0]];
            temp[1] = Sbox[temp[1]];
            temp[2] = Sbox[temp[2]];
            temp[3] = Sbox[temp[3]];

            
            temp[0] ^= RC[i/16 - 1];
        }

        
        EXkeys[i]   = EXkeys[i-16] ^ temp[0];
        EXkeys[i+1] = EXkeys[i-15] ^ temp[1];
        EXkeys[i+2] = EXkeys[i-14] ^ temp[2];
        EXkeys[i+3] = EXkeys[i-13] ^ temp[3];
    }
}



void Encrypt(uint8_t state[4][4], uint8_t roundKeys[176])
{
    int round = 0;
    uint8_t roundKey[4][4];
    int i, j;

    // Round 0 — load and apply original key
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            roundKey[j][i] = roundKeys[i*4 + j];  // column-major
    ADDRK(state, roundKey);

    // Rounds 1–9
    for(round = 1; round <= 9; round++)
    {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);

        for(i = 0; i < 4; i++)
            for(j = 0; j < 4; j++)
                roundKey[j][i] = roundKeys[round*16 + i*4 + j];

        ADDRK(state, roundKey);
    }

    // Round 10 — no MixColumns
    SubBytes(state);
    ShiftRows(state);
   // InvMixColumns(state);
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            roundKey[j][i] = roundKeys[160 + i*4 + j];
    ADDRK(state, roundKey);
}

